                <h1 class="titre_veil">Bootstrap</h1>
                <h1 class="h1_veil">Qu'est-ce que Bootstrap ?</h1>
                <p class="p_veil">
                C'est une collection d'outils qui est composée d'une multitude de codes HTML et CSS 
                et d'éléments graphiques, tels que des boutons, des formulaires, des barres de navigations et bien plus encore.
                Il sert principalement à créer un design plus épuré (sous forme de graphisme, d'animations...)
                pour les sites et applications Web. Sa première version est sortie le 19 août 2011,
                et sa dernière version disponible (la 3.3.7) est sortie le 25 juillet 2016.
                Bootstrap est écrit en JS, CSS, HTML et Sass.
                </p>
            </div>
            <div class="bloc_veil">
                <h1 class="h1_veil">Les fondateurs de Bootstrap</h1>
                <p class="p_veil">
                Ce sont les développeurs Mark Otto et Jacob Thornton qui l'ont conçu.
                Il font partie de la vague des développeurs qui se sont créés des profils sur les réseaux sociaux professionnels,
                tels que Twitter ou encore GitHub, afin de mieux se faire connaître.
                </p>
            </div>
            <div class="bloc_veil">
                <h1 class="h1_veil">Les évolutions depuis sa sortie</h1>
                <p class="p_veil">
                Le framework Bootstrap connait de nombreuses évolutions.
                Twitter le place comme étant un logiciel Open Source,
                le même mois et la même année de sa sortie, pour qu'il soit accessible à tous les internautes.
                En 2012, Bootstrap est classé comme le projet le plus populaire sur GitHub.
                Plus tard, sa deuxième version est sortie (voir la page officielle de téléchargement de la version 2.3.2 en cliquant ici).
                Cette dernière comprend ce que l'on appelle la conception de sites Web adaptatifs,
                ce qui permet aux sites et applications Web utilisant Bootstrap de s'adapter dynamiquement au format des supports depuis lesquels
                ils sont accédés (ordinateur, tablette et smartphone).
                De plus, la version comporte des éléments de personnalisation supplémentaires pour rendre ces projets plus agréables
                à visualiser et travailler.
                Enfin, Bootstrap comprend une série de feuilles de styles,
                appelées LESS, qui sont englobées dans une même feuille de style : bootstrap.less.
                Ainsi, les développeurs débutants ou experts peuvent sélectionner les composants dont ils ont besoin en modifiant
                leur feuille de style principale. Ils apparaissent dans la version 4 du langage SASS,
                et permettent la manipulation de variables, de fonctions, d'opérateurs et de sélecteurs
                (un peu comme JavaScript ou PHP dans ce domaine).
                </p>
            </div>
            <div class="bloc_veil">
                <h1 class="h1_veil">Conclusion</h1>
                <p class="p_veil">
                C'est un framework gratuit à télécharger, et facile à comprendre et à utiliser. Accessible en quelques clics,
                il fera les joies de tous les développeurs pour apprendre à coder des applications et
                sites Web et rendre leur design plus présentable.
                </p>
